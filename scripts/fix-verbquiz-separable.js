/* Comprehensive repair of verbquiz.json sentence word-order.

   The questions were generated by dropping the verb form into a position-2
   blank. That is correct for single-word Präsens/Präteritum forms, but WRONG for:
     • Perfekt  — the participle must move to the clause end
                  ("Ich bin heute glücklich gewesen.", not "Ich bin gewesen …").
     • Separable verbs — the prefix sits at the clause end with only the stem
                  in position 2 ("Wir heben genug ab.", not "Wir heben ab genug ab.").

   This script recomputes a correct, natural `sentence` for EVERY question and
   tags separable items so the quiz can accept the stem-only answer too. */

const fs = require('fs');
const path = require('path');

const FILE = path.join(__dirname, '..', 'public', 'data', 'verbquiz.json');
const PREFIXES = ['abzu','anzu','aufzu','auszu','einzu','mitzu','vorzu',
  'ab','an','auf','aus','ein','mit','nach','vor','zu','zurück','weg','fest',
  'los','her','hin','um','bei','durch','über','unter','wieder','fort','heim',
  'statt','zusammen','teil','frei','nieder','empor','voran','bereit','wahr'];
const PREFIX_SET = new Set(PREFIXES);
const PREFIX_ALT = PREFIXES.join('|');
const BLANK = /_{2,}|\[[^\]]*\]/;

const fill = (t, w) => t.replace(BLANK, w);
const cleanSpaces = (s) => s.replace(/\s{2,}/g, ' ').replace(/\s+([.,!?;:])/g, '$1').trim();

/* Insert `word` at the clause end: before the first clause boundary
   (comma or dash) if present, otherwise before the final punctuation. */
function insertBeforeBoundary(s, word) {
  s = s.replace(/\s+$/, '');
  const bm = s.match(/\s[–—-]\s|,/);     // first comma OR spaced dash
  if (bm) return s.slice(0, bm.index).replace(/\s+$/, '') + ' ' + word + s.slice(bm.index);
  const pm = s.match(/[.!?]+$/);
  if (pm) return s.slice(0, pm.index).replace(/\s+$/, '') + ' ' + word + s.slice(pm.index);
  return s + ' ' + word;
}

/* Remove a separable prefix wrongly left at the clause end (used for Perfekt,
   whose participle already contains the prefix). Only strips a standalone
   prefix that sits right before a comma or the final punctuation. */
function stripTrailingPrefix(s) {
  const re = new RegExp('\\s+(' + PREFIX_ALT + ')(\\s*)(,|[.!?]+\\s*$)');
  return s.replace(re, '$3');
}

const esc = (w) => w.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
/* Is `phrase` already a standalone word/phrase in the template?
   (German word chars include äöüß; the blank token has none, so it never matches.) */
const hasStandalone = (s, phrase) =>
  new RegExp('(^|[^\\wäöüÄÖÜß])' + esc(phrase) + '([^\\wäöüÄÖÜß]|$)').test(s);

const data = JSON.parse(fs.readFileSync(FILE, 'utf8'));
let perfekt = 0, separable = 0, simple = 0;

const REFLEX_BY_PERSON = {
  'ich': 'mich', 'du': 'dich', 'er/sie/es': 'sich',
  'wir': 'uns', 'ihr': 'euch', 'sie/Sie': 'sich',
};
const REFLEX_PRONOUNS = new Set(['mich', 'dich', 'sich', 'uns', 'euch', 'mir', 'dir']);

for (const item of data) {
  const template = (item.q || '').split('\n')[0];
  const correct  = (item.correct || '').trim();
  const tokens   = correct.split(/\s+/);
  delete item.sep; delete item.alt;

  // Reflexive verbs ("sich freuen", …) need the person-correct pronoun present —
  // but never add a second one if the template already carries a reflexive pronoun.
  const templateHasAnyRefl = [...REFLEX_PRONOUNS].some(p => hasStandalone(template, p));
  const reflPron = /^sich\s/.test(item.verb || '') ? REFLEX_BY_PERSON[item.person] : null;
  const tokenHasRefl = tokens.some(t => REFLEX_PRONOUNS.has(t));
  const needRefl = reflPron && !tokenHasRefl && !templateHasAnyRefl;

  let sentence;

  if (tokens.length === 1) {
    // Single conjugated word → drops straight into the position-2 blank.
    sentence = fill(template, needRefl ? `${correct} ${reflPron}` : correct);
    simple++;
  } else if (item.tense === 'Perfekt') {
    // Perfekt: auxiliary stays in position 2, participle moves to the clause end.
    // A reflexive pronoun goes after the auxiliary if not already present.
    const aux        = tokens[0];
    const participle = tokens[tokens.length - 1];
    const middle     = tokens.slice(1, -1).filter(t => !hasStandalone(template, t));
    if (needRefl) middle.unshift(reflPron);
    const auxFill    = [aux, ...middle].join(' ');
    if (hasStandalone(template, participle)) {
      sentence = fill(template, auxFill);       // participle already placed correctly
    } else {
      sentence = insertBeforeBoundary(stripTrailingPrefix(fill(template, auxFill)), participle);
    }
    perfekt++;
  } else {
    // Präsens/Präteritum multi-token: the FINITE verb (first token) fills the blank.
    // A reflexive pronoun goes into the blank after the verb if the template lacks it;
    // a separable prefix is re-placed at the clause end if the template lacks it.
    const finite = tokens[0];
    const extra = [];          // tokens to drop into the blank next to the verb
    const prefixes = [];       // separable prefixes to place at the clause end
    let isSep = false;
    if (needRefl) extra.push(reflPron);
    for (const t of tokens.slice(1)) {
      if (PREFIX_SET.has(t)) { isSep = true; if (!hasStandalone(template, t)) prefixes.push(t); }
      else if (REFLEX_PRONOUNS.has(t)) { if (!hasStandalone(template, t) && !templateHasAnyRefl) extra.push(t); }
      else if (!hasStandalone(template, t)) extra.push(t);   // other missing clitic
    }
    let s = fill(template, [finite, ...extra].join(' '));
    for (const p of prefixes) s = insertBeforeBoundary(s, p);
    sentence = s;
    item.alt = finite;                          // accept the conjugated verb alone too
    if (isSep) { item.sep = true; separable++; } else { simple++; }
  }

  item.sentence = cleanSpaces(sentence);
}

fs.writeFileSync(FILE, JSON.stringify(data));
console.log(`Repaired ${data.length} questions — simple: ${simple}, Perfekt: ${perfekt}, separable: ${separable}`);
